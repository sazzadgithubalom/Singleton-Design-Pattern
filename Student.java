package com.alomindia.model;

public class Student {
	
	private String stName;
	private Integer stRoll;
	private Double stFees;
	
	
	public String getStName() {
		return stName;
	}
	public void setStName(String stName) {
		this.stName = stName;
	}
	public Integer getStRoll() {
		return stRoll;
	}
	public void setStRoll(Integer stRoll) {
		this.stRoll = stRoll;
	}
	public Double getStFees() {
		return stFees;
	}
	public void setStFees(Double stFees) {
		this.stFees = stFees;
	}
	
	

}
